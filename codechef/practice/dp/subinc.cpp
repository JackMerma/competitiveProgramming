void solve(){
    int n; cin>>n;
    vector<ll> arr(n+1);

    for(int i=0;i<n;i++)
        cin>>arr[i];

    ulli ans=0;
    int ini=0;
    for(int i=1;i<n+1;i++){
        if(arr[i]<arr[i-1]){
            ans+=((i-ini-1)*(i-ini))/2+i-ini;
            ini=i;
        }
    }
    //ans+=((n-ini-1)*(n-ini))/2+n-ini;
    cout<<ans<<endl;
}

