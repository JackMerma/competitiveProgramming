#define INF 2e9
#define len length
#define endl "\n"

void solve(){
    // 25 50 75 00
    string str; cin>>str;
    int n=str.length();
    int ans=INF;
    
    int aux=0;
    int f=0;
    for(int i=n-1;i>=0;i--){
        if(str[i]=='0'){
            if(f){
                ans=min(ans, aux+f-i-1);
                break;
            }else{
                aux=n-i-1;
                f=i;
            }
        }
    }

    aux=0;
    f=0;
    for(int i=n-1;i>=0;i--){
        if(str[i]=='0' && !f)
            f=i, aux=n-i-1;
        if(str[i]=='5' && f){
            ans=min(ans, aux+f-i-1);
            break;
        }
    }

    aux=0;
    f=0;
    for(int i=n-1;i>=0;i--){
        if(str[i]=='5' && !f){
            f=i;
            aux=n-i-1;
        }
        if((str[i]=='2'||str[i]=='7')&&f){
            ans=min(ans, aux+f-i-1);
            break;
        }
    }

    cout<<ans<<endl;
}

int main(){
	ios_base::sync_with_stdio(false); cin.tie(NULL);
    clock_t z = clock();

    ll t; cin>>t;

    while(t--) 
        solve();

#ifndef ONLINE_JUDGE
    cout<<endl<<"Tiempo total: "<<fixed<<setprecision(3)<<(double)(clock()-z)/CLOCKS_PER_SEC<<endl;
#endif

	return 0;
}

