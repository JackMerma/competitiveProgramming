#define len length
#define endl "\n"

void solve(){
    int n, q; cin>>n>>q;
    string str; cin>>str;
    ll dp[n+1];
    dp[0]=0;
    dp[1]=str[0]-'a'+1;

    for(int i=2;i<=n;i++)
        dp[i]=dp[i-1]+str[i-1]-'a'+1;

    for(int i=0;i<q;i++){
        int l, r; cin>>l>>r;
        cout<<dp[r]-dp[l-1]<<endl;
    }
}

int main(){
	ios_base::sync_with_stdio(false); cin.tie(NULL);
    clock_t z = clock();

    solve();

#ifndef ONLINE_JUDGE
    cout<<endl<<"Tiempo total: "<<fixed<<setprecision(3)<<(double)(clock()-z)/CLOCKS_PER_SEC<<endl;
#endif

    return 0;
}

