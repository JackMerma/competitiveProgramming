#include <bits/stdc++.h>
using namespace std;

#define debug(...) fprintf(stderr, __VA_ARGS__), fflush(stderr)
#define ull unsigned long long
#define ll long long
#define ld long double
#define ulli unsigned long long int
#define umap unordered_map
#define uset unordered_set
#define pqueue priority_queue
#define pi 3.141592653589793238462
#define INF 2e9
#define len length
#define endl "\n"

void solve(){
    int n; cin>>n;
    vector<ll> dp(n);

    for(int i=0;i<n;i++)
        cin>>dp[i];

    int ci, cp, cc;
    ci = cp = cc = 0;

    ull ans = 0;

    for(int i=0;i<n;i++){
        if(dp[i]<0){
            ans+=abs(dp[i])-1;
            dp[i]=-1;
            ci++;
        }else if(dp[i]>0){
            ans+=dp[i]-1;
            dp[i]=1;
            cp++;
        }else{
            cc++;
        }
    }

    if(!(ci%2)){
        ans+=cc;
        cout<<ans<<endl;
    }else{
        if(cc){
            ans+=cc;
            cout<<ans<<endl;
        }else{
            ans+=2;
            cout<<ans<<endl;
        }
    }
}

int main(){
	ios_base::sync_with_stdio(false); cin.tie(NULL);
    clock_t z = clock();

    solve();

#ifndef ONLINE_JUDGE
    cout<<endl<<"Tiempo total: "<<fixed<<setprecision(3)<<(double)(clock()-z)/CLOCKS_PER_SEC<<endl;
#endif

    return 0;
}

