#define INF 2e9
#define len length
#define endl "\n"

void solve(){
    int n, m; cin>>n>>m;
    vector<ll> arr(n);

    for(int i=0;i<n;i++) 
        cin>>arr[i];

    ll dp[2][n];
    dp[0][0]=dp[1][n-1]=0;

    for(int i=1;i<n;i++){
        dp[0][i]=(arr[i]<arr[i-1])?dp[0][i-1]+arr[i-1]-arr[i]:dp[0][i-1];
        dp[1][n-i-1]=(arr[n-i-1]<arr[n-i])?dp[1][n-i]+arr[n-i]-arr[n-i-1]:dp[1][n-i];
    }

    int a, b;
    for(int i=0;i<m;i++){
        cin>>a>>b;
        a--, b--;
        if(a<b)
            cout<<dp[0][b]-dp[0][a]<<endl;
        else
            cout<<dp[1][b]-dp[1][a]<<endl;
    }
}

int main(){
	ios_base::sync_with_stdio(false); cin.tie(NULL);
    clock_t z = clock();
    solve();

#ifndef ONLINE_JUDGE
    cout<<endl<<"Tiempo total: "<<fixed<<setprecision(3)<<(double)(clock()-z)/CLOCKS_PER_SEC<<endl;
#endif

    return 0;
}

