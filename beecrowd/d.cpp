bool caso(pair<int, int> a, pair<int, int> b){
    return a.second-a.first<b.second-b.first;
}

void solve(){
    ll n, g; cin>>n>>g;
    vector<pair<int, int>> arr;

    ll ans=0;
    for(int i=0;i<n;i++){
        int a, b; cin>>a>>b;
        if(a>b)
            ans+=3;
        else
            arr.push_back(pair<int, int>(a,b));
    }
    sort(arr.begin(),arr.end(), caso);

    bool puede=true;
    for(int i=0;i<(int)arr.size();i++){
        if(arr[i].second-arr[i].first>g)
            puede=false;

        if(puede){
            if(arr[i].second-arr[i].first==g){
                ans++;
                g--;
            }else{
                ans+=3;
                g-=arr[i].second-arr[i].first+1;
            }
        }else{
            if(arr[i].second==arr[i].first)
                ans++;
        }
    }
    cout<<ans<<endl;
}

int main(){
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    clock_t z = clock();

    ll t; cin>>t;

    while(t--) 
        solve();

#ifndef ONLINE_JUDGE
    cout<<endl<<"Tiempo total: "<<fixed<<setprecision(3)<<(double)(clock()-z)/CLOCKS_PER_SEC<<endl;
#endif

    return 0;
}

